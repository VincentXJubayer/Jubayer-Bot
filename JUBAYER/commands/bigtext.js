module.exports.run = async ({ api, event, args }) => {
  if (!args.length)
    return api.sendMessage(
      "‚ùå Please enter the text after the command.\nExample: .bigtext hello",
      event.threadID,
      event.messageID
    );

  const input = args.join(" ").toUpperCase();

  let outputLines = ["", "", "", "", "", ""];

  for (const ch of input) {
    const letterArt = FONT[ch] || FONT[" "];
    for (let i = 0; i < 6; i++) {
      outputLines[i] += letterArt[i] + "  ";
    }
  }

  const msg = "```\n" + outputLines.join("\n") + "\n```";

  return api.sendMessage(msg, event.threadID, event.messageID);
};
